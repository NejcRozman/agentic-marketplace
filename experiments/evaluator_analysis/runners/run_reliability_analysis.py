#!/usr/bin/env python3
"""
Run evaluator reliability analysis on an existing generated dataset.

Reliability question:
How much does ServiceEvaluator's score vary when the exact same service output
is evaluated repeatedly?

Pipeline:
1. Load dataset records generated by generate_dataset.py.
2. Re-evaluate each sample output N times with ServiceEvaluator.
3. Compute per-sample and overall variance statistics.
4. Persist raw runs + summary artifacts.
"""

from __future__ import annotations

import argparse
import asyncio
import contextlib
import csv
import json
import logging
import os
import random
import re
import shutil
import statistics
import sys
import time
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Optional

import yaml

REPO_ROOT = Path(__file__).resolve().parents[3]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from agents.config import Config
from agents.consumer_agent.evaluator import ServiceEvaluator

logger = logging.getLogger("evaluator_reliability")


@dataclass
class ReliabilityAnalysisConfig:
    analysis_id: str
    dataset_jsonl: str
    output_dir: str
    repeats_per_sample: int
    max_samples: Optional[int]
    sample_ids: Optional[List[str]]
    include_partial_fallback_samples: bool
    randomize_sample_order: bool
    random_seed: int
    evaluation_timeout_seconds: Optional[float]
    heartbeat_interval_seconds: float
    overwrite: bool


class ReliabilityAnalyzer:
    def __init__(self, cfg: ReliabilityAnalysisConfig):
        self.cfg = cfg
        self.runtime_config = Config()

        if not self.runtime_config.openrouter_api_key:
            raise ValueError("OPENROUTER_API_KEY is required for evaluator reliability analysis")

        self.evaluator = ServiceEvaluator(self.runtime_config)

        self.dataset_path = self._abs_path(cfg.dataset_jsonl)
        self.output_root = self._abs_path(cfg.output_dir) / cfg.analysis_id
        self.raw_runs_path = self.output_root / "raw_runs.jsonl"
        self.sample_summary_json_path = self.output_root / "sample_summary.json"
        self.sample_summary_csv_path = self.output_root / "sample_summary.csv"
        self.overall_summary_path = self.output_root / "overall_summary.json"

    @staticmethod
    def _abs_path(path_str: str) -> Path:
        path = Path(path_str)
        if path.is_absolute():
            return path
        return REPO_ROOT / path

    def _prepare_output_dir(self) -> None:
        if self.output_root.exists() and not self.cfg.overwrite:
            raise FileExistsError(
                f"Output already exists: {self.output_root}. Use overwrite=true or --overwrite."
            )

        if self.cfg.overwrite and self.output_root.exists():
            shutil.rmtree(self.output_root)

        self.output_root.mkdir(parents=True, exist_ok=True)

    @staticmethod
    def _format_duration(seconds: float) -> str:
        total = max(0, int(round(seconds)))
        hours, rem = divmod(total, 3600)
        minutes, secs = divmod(rem, 60)
        if hours > 0:
            return f"{hours}h {minutes}m {secs}s"
        if minutes > 0:
            return f"{minutes}m {secs}s"
        return f"{secs}s"

    async def _evaluate_with_observability(
        self,
        requirements: Dict[str, Any],
        service_output: Dict[str, Any],
        run_label: str,
    ) -> Dict[str, Any]:
        """
        Execute one evaluation with optional timeout and periodic heartbeat logs.

        Returns a normalized evaluation-like payload, including duration_seconds.
        """
        started = time.time()
        timeout = self.cfg.evaluation_timeout_seconds
        heartbeat = max(1.0, float(self.cfg.heartbeat_interval_seconds))

        task = asyncio.create_task(self.evaluator.evaluate(requirements, service_output))
        while True:
            elapsed = time.time() - started
            remaining: Optional[float] = None
            if timeout is not None:
                remaining = timeout - elapsed
                if remaining <= 0:
                    task.cancel()
                    with contextlib.suppress(asyncio.CancelledError):
                        await task
                    return {
                        "rating": None,
                        "quality_scores": {},
                        "error": f"Timed out after {timeout:.1f}s",
                        "duration_seconds": round(time.time() - started, 3),
                    }

            wait_for = heartbeat if remaining is None else min(heartbeat, remaining)
            done, _ = await asyncio.wait({task}, timeout=wait_for)
            if task in done:
                try:
                    result = task.result()
                except Exception as exc:
                    return {
                        "rating": None,
                        "quality_scores": {},
                        "error": str(exc),
                        "duration_seconds": round(time.time() - started, 3),
                    }

                duration = round(time.time() - started, 3)
                if not isinstance(result, dict):
                    return {
                        "rating": None,
                        "quality_scores": {},
                        "error": "Evaluator returned non-dict result",
                        "duration_seconds": duration,
                    }

                result.setdefault("rating", None)
                result.setdefault("quality_scores", {})
                result.setdefault("error", None)
                result["duration_seconds"] = duration
                return result

            elapsed = time.time() - started
            if timeout is None:
                logger.info(
                    "Heartbeat: %s still running (elapsed=%.1fs)",
                    run_label,
                    elapsed,
                )
            else:
                logger.info(
                    "Heartbeat: %s still running (elapsed=%.1fs, timeout=%.1fs)",
                    run_label,
                    elapsed,
                    timeout,
                )

    def _load_dataset(self) -> List[Dict[str, Any]]:
        if not self.dataset_path.exists():
            raise FileNotFoundError(f"Dataset jsonl not found: {self.dataset_path}")

        rows: List[Dict[str, Any]] = []
        with open(self.dataset_path, "r", encoding="utf-8") as f:
            for line_no, line in enumerate(f, start=1):
                line = line.strip()
                if not line:
                    continue
                try:
                    row = json.loads(line)
                except json.JSONDecodeError as exc:
                    raise ValueError(f"Invalid JSON at {self.dataset_path}:{line_no}: {exc}") from exc
                rows.append(row)

        filtered: List[Dict[str, Any]] = []
        selected_ids = set(self.cfg.sample_ids or [])

        for row in rows:
            sample_id = row.get("sample_id")
            if selected_ids and sample_id not in selected_ids:
                continue

            if not row.get("success", False):
                continue

            requirements = row.get("requirements")
            result = row.get("result")
            if not isinstance(requirements, dict) or not isinstance(result, dict):
                continue

            responses = result.get("responses", [])
            if not responses:
                continue

            if (
                not self.cfg.include_partial_fallback_samples
                and bool(result.get("partial_fallback_used"))
            ):
                continue

            filtered.append(row)

        if self.cfg.randomize_sample_order:
            rng = random.Random(self.cfg.random_seed)
            rng.shuffle(filtered)

        if self.cfg.max_samples is not None:
            filtered = filtered[: self.cfg.max_samples]

        return filtered

    @staticmethod
    def _append_jsonl(path: Path, payload: Dict[str, Any]) -> None:
        with open(path, "a", encoding="utf-8") as f:
            f.write(json.dumps(payload, ensure_ascii=False) + "\n")

    @staticmethod
    def _numeric_stats(values: List[float]) -> Dict[str, Optional[float]]:
        if not values:
            return {
                "count": 0,
                "mean": None,
                "variance": None,
                "std": None,
                "min": None,
                "max": None,
                "range": None,
            }

        if len(values) == 1:
            v = float(values[0])
            return {
                "count": 1,
                "mean": v,
                "variance": 0.0,
                "std": 0.0,
                "min": v,
                "max": v,
                "range": 0.0,
            }

        mean_v = statistics.mean(values)
        var_v = statistics.pvariance(values)
        std_v = statistics.pstdev(values)
        min_v = min(values)
        max_v = max(values)
        return {
            "count": len(values),
            "mean": float(mean_v),
            "variance": float(var_v),
            "std": float(std_v),
            "min": float(min_v),
            "max": float(max_v),
            "range": float(max_v - min_v),
        }

    @staticmethod
    def _flatten_sample_summary_rows(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        dim_names = set()
        for row in rows:
            dim_names.update(row.get("dimension_stats", {}).keys())

        ordered_dims = sorted(dim_names)
        flat: List[Dict[str, Any]] = []
        for row in rows:
            base = {
                "sample_id": row.get("sample_id"),
                "source_pdf": row.get("source_pdf"),
                "runs_total": row.get("runs_total"),
                "runs_successful": row.get("runs_successful"),
                "runs_failed": row.get("runs_failed"),
                "rating_mean": row.get("rating_stats", {}).get("mean"),
                "rating_variance": row.get("rating_stats", {}).get("variance"),
                "rating_std": row.get("rating_stats", {}).get("std"),
                "rating_min": row.get("rating_stats", {}).get("min"),
                "rating_max": row.get("rating_stats", {}).get("max"),
                "rating_range": row.get("rating_stats", {}).get("range"),
                "duration_mean_seconds": row.get("duration_stats", {}).get("mean"),
            }

            dim_stats = row.get("dimension_stats", {})
            for dim in ordered_dims:
                ds = dim_stats.get(dim, {})
                base[f"{dim}_mean"] = ds.get("mean")
                base[f"{dim}_variance"] = ds.get("variance")
                base[f"{dim}_std"] = ds.get("std")
                base[f"{dim}_min"] = ds.get("min")
                base[f"{dim}_max"] = ds.get("max")

            flat.append(base)

        return flat

    @staticmethod
    def _write_csv(path: Path, rows: List[Dict[str, Any]]) -> None:
        if not rows:
            return

        fieldnames = list(rows[0].keys())
        with open(path, "w", newline="", encoding="utf-8") as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            for row in rows:
                writer.writerow(row)

    async def run(self, dry_run: bool = False) -> Dict[str, Any]:
        samples = self._load_dataset()

        if not samples:
            raise RuntimeError("No eligible dataset rows found for reliability analysis")

        logger.info(
            "Loaded %s samples for reliability analysis (repeats_per_sample=%s)",
            len(samples),
            self.cfg.repeats_per_sample,
        )

        if dry_run:
            return {
                "analysis_id": self.cfg.analysis_id,
                "dataset_path": str(self.dataset_path),
                "sample_count": len(samples),
                "repeats_per_sample": self.cfg.repeats_per_sample,
                "planned_total_runs": len(samples) * self.cfg.repeats_per_sample,
            }

        self._prepare_output_dir()

        started = time.time()
        per_sample_runs: Dict[str, List[Dict[str, Any]]] = {row["sample_id"]: [] for row in samples}
        total_planned_runs = len(samples) * self.cfg.repeats_per_sample
        completed_runs = 0

        for sample_idx, sample in enumerate(samples, start=1):
            sample_id = sample["sample_id"]
            source_pdf = sample.get("source_pdf")
            requirements = sample["requirements"]
            service_output = sample["result"]

            logger.info(
                "[%s/%s] Running reliability loops for %s",
                sample_idx,
                len(samples),
                sample_id,
            )

            for repeat_idx in range(1, self.cfg.repeats_per_sample + 1):
                run_label = (
                    f"sample {sample_id} [{sample_idx}/{len(samples)}], "
                    f"repeat {repeat_idx}/{self.cfg.repeats_per_sample}"
                )
                logger.info("Starting %s", run_label)

                evaluation = await self._evaluate_with_observability(
                    requirements=requirements,
                    service_output=service_output,
                    run_label=run_label,
                )
                duration = float(evaluation.get("duration_seconds", 0.0))

                run_record = {
                    "analysis_id": self.cfg.analysis_id,
                    "sample_id": sample_id,
                    "source_pdf": source_pdf,
                    "repeat_idx": repeat_idx,
                    "duration_seconds": duration,
                    "rating": evaluation.get("rating"),
                    "quality_scores": evaluation.get("quality_scores", {}),
                    "error": evaluation.get("error"),
                }

                per_sample_runs[sample_id].append(run_record)
                self._append_jsonl(self.raw_runs_path, run_record)

                completed_runs += 1
                avg_run_seconds = (time.time() - started) / completed_runs
                remaining_runs = total_planned_runs - completed_runs
                eta_seconds = remaining_runs * avg_run_seconds

                if run_record["error"]:
                    logger.warning(
                        "Finished %s with error in %.1fs: %s",
                        run_label,
                        duration,
                        run_record["error"],
                    )
                else:
                    logger.info(
                        "Finished %s in %.1fs (rating=%s)",
                        run_label,
                        duration,
                        run_record["rating"],
                    )

                logger.info(
                    "Progress: %s/%s runs (%.1f%%), avg %.1fs/run, ETA ~%s",
                    completed_runs,
                    total_planned_runs,
                    (completed_runs / total_planned_runs) * 100.0,
                    avg_run_seconds,
                    self._format_duration(eta_seconds),
                )

        sample_summaries: List[Dict[str, Any]] = []
        all_rating_values: List[float] = []
        sample_variances: List[float] = []
        sample_stds: List[float] = []
        total_success_runs = 0
        total_failed_runs = 0

        for sample in samples:
            sample_id = sample["sample_id"]
            source_pdf = sample.get("source_pdf")
            runs = per_sample_runs[sample_id]

            successful = [r for r in runs if r.get("error") is None and r.get("rating") is not None]
            failed = [r for r in runs if r.get("error") is not None]

            ratings = [float(r["rating"]) for r in successful]
            durations = [float(r["duration_seconds"]) for r in runs]

            rating_stats = self._numeric_stats(ratings)
            duration_stats = self._numeric_stats(durations)

            if rating_stats["variance"] is not None:
                sample_variances.append(float(rating_stats["variance"]))
            if rating_stats["std"] is not None:
                sample_stds.append(float(rating_stats["std"]))
            all_rating_values.extend(ratings)

            total_success_runs += len(successful)
            total_failed_runs += len(failed)

            dim_values: Dict[str, List[float]] = {}
            for r in successful:
                for dim, score in (r.get("quality_scores") or {}).items():
                    if isinstance(score, (int, float)):
                        dim_values.setdefault(dim, []).append(float(score))

            dimension_stats = {dim: self._numeric_stats(vals) for dim, vals in sorted(dim_values.items())}

            sample_summaries.append(
                {
                    "sample_id": sample_id,
                    "source_pdf": source_pdf,
                    "runs_total": len(runs),
                    "runs_successful": len(successful),
                    "runs_failed": len(failed),
                    "rating_stats": rating_stats,
                    "duration_stats": duration_stats,
                    "dimension_stats": dimension_stats,
                }
            )

        overall = {
            "analysis_id": self.cfg.analysis_id,
            "created_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
            "dataset_path": str(self.dataset_path),
            "sample_count": len(samples),
            "repeats_per_sample": self.cfg.repeats_per_sample,
            "total_runs": len(samples) * self.cfg.repeats_per_sample,
            "successful_runs": total_success_runs,
            "failed_runs": total_failed_runs,
            "overall_rating_stats": self._numeric_stats(all_rating_values),
            "within_sample_variance_stats": self._numeric_stats(sample_variances),
            "within_sample_std_stats": self._numeric_stats(sample_stds),
            "duration_seconds_total": round(time.time() - started, 3),
            "config": {
                "max_samples": self.cfg.max_samples,
                "sample_ids": self.cfg.sample_ids,
                "include_partial_fallback_samples": self.cfg.include_partial_fallback_samples,
                "randomize_sample_order": self.cfg.randomize_sample_order,
                "random_seed": self.cfg.random_seed,
                "evaluation_timeout_seconds": self.cfg.evaluation_timeout_seconds,
                "heartbeat_interval_seconds": self.cfg.heartbeat_interval_seconds,
            },
            "output_files": {
                "raw_runs_jsonl": str(self.raw_runs_path),
                "sample_summary_json": str(self.sample_summary_json_path),
                "sample_summary_csv": str(self.sample_summary_csv_path),
                "overall_summary_json": str(self.overall_summary_path),
            },
        }

        # Top unstable samples by rating std for quick diagnostics.
        unstable = sorted(
            sample_summaries,
            key=lambda s: (s.get("rating_stats", {}).get("std") or 0.0),
            reverse=True,
        )
        overall["top_unstable_samples"] = [
            {
                "sample_id": s["sample_id"],
                "source_pdf": s.get("source_pdf"),
                "rating_std": s.get("rating_stats", {}).get("std"),
                "rating_variance": s.get("rating_stats", {}).get("variance"),
                "rating_mean": s.get("rating_stats", {}).get("mean"),
            }
            for s in unstable[: min(10, len(unstable))]
        ]

        with open(self.sample_summary_json_path, "w", encoding="utf-8") as f:
            json.dump(sample_summaries, f, indent=2, ensure_ascii=False)

        with open(self.overall_summary_path, "w", encoding="utf-8") as f:
            json.dump(overall, f, indent=2, ensure_ascii=False)

        flat_rows = self._flatten_sample_summary_rows(sample_summaries)
        self._write_csv(self.sample_summary_csv_path, flat_rows)

        logger.info("Reliability analysis complete: %s", json.dumps(overall, indent=2))
        return overall


def _expand_env_vars(raw_text: str) -> str:
    def repl(match: re.Match[str]) -> str:
        var_name = match.group(1)
        value = os.getenv(var_name)
        if value is None:
            raise ValueError(f"Environment variable {var_name} referenced in config but not set")
        return value

    return re.sub(r"\$\{([A-Za-z_][A-Za-z0-9_]*)\}", repl, raw_text)


def load_config(path: Path) -> ReliabilityAnalysisConfig:
    raw = path.read_text(encoding="utf-8")
    expanded = _expand_env_vars(raw)
    data = yaml.safe_load(expanded) or {}

    section = data.get("reliability_analysis", {})
    if not section:
        raise ValueError("Missing 'reliability_analysis' section in config")

    sample_ids = section.get("sample_ids")
    if sample_ids is not None and not isinstance(sample_ids, list):
        raise ValueError("sample_ids must be a list when provided")

    repeats = int(section.get("repeats_per_sample", 5))
    if repeats < 1:
        raise ValueError("repeats_per_sample must be >= 1")

    heartbeat_interval_seconds = float(section.get("heartbeat_interval_seconds", 30))
    if heartbeat_interval_seconds <= 0:
        raise ValueError("heartbeat_interval_seconds must be > 0")

    timeout_raw = section.get("evaluation_timeout_seconds")
    evaluation_timeout_seconds: Optional[float]
    if timeout_raw is None:
        evaluation_timeout_seconds = None
    else:
        evaluation_timeout_seconds = float(timeout_raw)
        if evaluation_timeout_seconds <= 0:
            raise ValueError("evaluation_timeout_seconds must be > 0 when provided")

    return ReliabilityAnalysisConfig(
        analysis_id=section.get("analysis_id", "reliability_v1"),
        dataset_jsonl=section.get(
            "dataset_jsonl",
            "experiments/evaluator_analysis/data/datasets/service_outputs_v1.jsonl",
        ),
        output_dir=section.get("output_dir", "experiments/evaluator_analysis/data/reliability"),
        repeats_per_sample=repeats,
        max_samples=section.get("max_samples"),
        sample_ids=sample_ids,
        include_partial_fallback_samples=bool(section.get("include_partial_fallback_samples", True)),
        randomize_sample_order=bool(section.get("randomize_sample_order", False)),
        random_seed=int(section.get("random_seed", 42)),
        evaluation_timeout_seconds=evaluation_timeout_seconds,
        heartbeat_interval_seconds=heartbeat_interval_seconds,
        overwrite=bool(section.get("overwrite", False)),
    )


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run evaluator reliability analysis on generated dataset")
    parser.add_argument(
        "--config",
        type=Path,
        default=REPO_ROOT / "experiments/evaluator_analysis/configs/reliability_analysis.yaml",
        help="Path to reliability analysis YAML config",
    )
    parser.add_argument(
        "--max-samples",
        type=int,
        default=None,
        help="Optional runtime override for number of dataset samples",
    )
    parser.add_argument(
        "--repeats",
        type=int,
        default=None,
        help="Optional runtime override for repeats per sample",
    )
    parser.add_argument(
        "--overwrite",
        action="store_true",
        help="Overwrite previous outputs for this analysis_id",
    )
    parser.add_argument(
        "--evaluation-timeout-seconds",
        type=float,
        default=None,
        help="Optional timeout per evaluator call; timed-out calls are recorded as failed and analysis continues",
    )
    parser.add_argument(
        "--heartbeat-interval-seconds",
        type=float,
        default=None,
        help="Heartbeat log interval while waiting for evaluator calls",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Only validate input/config and print planned run size; do not call evaluator",
    )
    parser.add_argument(
        "--log-level",
        type=str,
        default="INFO",
        choices=["DEBUG", "INFO", "WARNING", "ERROR"],
    )
    return parser.parse_args()


async def _async_main() -> int:
    args = parse_args()

    logging.basicConfig(
        level=getattr(logging, args.log_level),
        format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
    )

    cfg = load_config(args.config)
    if args.max_samples is not None:
        cfg.max_samples = args.max_samples
    if args.repeats is not None:
        cfg.repeats_per_sample = args.repeats
    if args.overwrite:
        cfg.overwrite = True
    if args.evaluation_timeout_seconds is not None:
        if args.evaluation_timeout_seconds <= 0:
            raise ValueError("--evaluation-timeout-seconds must be > 0")
        cfg.evaluation_timeout_seconds = float(args.evaluation_timeout_seconds)
    if args.heartbeat_interval_seconds is not None:
        if args.heartbeat_interval_seconds <= 0:
            raise ValueError("--heartbeat-interval-seconds must be > 0")
        cfg.heartbeat_interval_seconds = float(args.heartbeat_interval_seconds)

    analyzer = ReliabilityAnalyzer(cfg)
    result = await analyzer.run(dry_run=args.dry_run)

    if args.dry_run:
        logger.info("Dry-run plan: %s", json.dumps(result, indent=2))

    return 0


def main() -> int:
    try:
        return asyncio.run(_async_main())
    except KeyboardInterrupt:
        logger.warning("Interrupted by user")
        return 130
    except Exception as exc:
        logger.error("Reliability analysis failed: %s", exc, exc_info=True)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
